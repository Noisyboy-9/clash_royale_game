package cards.utils;

/**
 * The type Position.
 */
public record Position(int x, int y) {
    /**
     * Gets x.
     *
     * @return the x
     */
    public int getX() {
        return x;
    }

    /**
     * Gets y.
     *
     * @return the y
     */
    public int getY() {
        return y;
    }
}
